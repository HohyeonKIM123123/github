


fruits = ['사괴', '바나나', '체리', '딸기']


# export all of the list

# 1. for in sentence
for fruit in fruits :   # in this sentences "fruit" is ingredient not a list
    print(fruit)

# 2. range() function
for i in range (len(fruits)) :
    print(fruits[i])

# 3. enumerate () function
for index, fruit in enumerate (fruits):
    print(f'{index}, {fruit}')


